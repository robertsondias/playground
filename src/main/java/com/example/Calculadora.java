package com.example;

public class Calculadora {
    
    public int somar(int x, int y){
        return x + y;
    }

    public int subtrair(int x, int y) {
        return x - y;
    }

    public int divisao(int x, int y) {
        return x / y;
    }

    public int multiplicar(int x, int y){
        return x * y;
    }

}
